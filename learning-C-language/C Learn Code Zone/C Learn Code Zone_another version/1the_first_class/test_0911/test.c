 //学生
struct Stu  //用类型创建变量
{
	//成员， 
	char name[20];
	int age;
	char sex[10];
	char tale[12];
};
void print(struct Stu* ps)
{
	printf("%s %d %s %s", ps->name, ps->age, ps->sex, ps->tale);
	//结构体指针变量->成员名
	printf("%s %d %s %s", (*ps).name, (*ps).age, (*ps).sex, (*ps).tale);
}
int main()
{
	struct Stu s = {"zhangsan",20,"man","13286948411"};//初始化
	printf("%s %d %s %s",  s.name , s.age , s.sex, s.tale);
	//  . ：左边结构体对象.成员名
	print(&s);
	return 0;
}

